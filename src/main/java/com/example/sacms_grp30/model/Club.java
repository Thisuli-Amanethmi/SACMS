package com.example.sacms_grp30.model;

public class Club {
    private String clubId;
    private String clubAdvisorId;
    private String clubName;
    private String clubCategory;
    private String description;

    public String getClubId() {
        return clubId;
    }

    public void setClubId(String clubId) {
        this.clubId = clubId;
    }

    public String getClubAdvisorId() {
        return clubAdvisorId;
    }

    public void setClubAdvisorId(String clubAdvisorId) {
        this.clubAdvisorId = clubAdvisorId;
    }

    public String getClubName() {
        return clubName;
    }

    public void setClubName(String clubName) {
        this.clubName = clubName;
    }

    public String getClubCategory() {
        return clubCategory;
    }

    public void setClubCategory(String clubCategory) {
        this.clubCategory = clubCategory;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
